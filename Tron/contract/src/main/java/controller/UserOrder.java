package controller;

public enum UserOrder {
	   
	/** Turn Player 1 Lightcycle of 90° right & P2 go forward automatically*/
    P1RIGHT,

	/** Turn Player 1 Lightcycle of 90° left & P2 go forward automatically*/
    P1LEFT,
    
	/** Turn Player 2 Lightcycle of 90° right & P1 go forward automatically*/
    P2RIGHT,

	/** Turn Player 2 Lightcycle of 90° left & P1 go forward automatically*/
    P2LEFT,
    
	/** Player 1 & 2 don't press any key they just go forward automatically */
    NOP,


}